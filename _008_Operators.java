public class _008_Operators {
    public static void main(String args[]) {
        int a = 4;
        int b = 6 + a;
        System.out.println(b);
        b = 6 % a; // Modular operator
        System.out.println(b);
        b += 5;
        System.out.println(b);
        System.out.println(8 == 6); // Comparison operator

        System.out.println(2.3f % 1.3);
    }
}
